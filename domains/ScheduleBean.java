package com.bitcamp.domains;

public class ScheduleBean {
	private String month, year;
	
	public void setMonth(String month) {
		this.month = month;
	}
	public String getMonth(){
		return this.month;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getYear() {
		return this.year;
	}

}
